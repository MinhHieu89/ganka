namespace Optical.Domain.Enums;

/// <summary>
/// Material used in the construction of an eyeglass frame.
/// Determines durability, weight, and hypoallergenic properties.
/// </summary>
public enum FrameMaterial
{
    /// <summary>Metal (Kim loại) — durable, adjustable, classic look</summary>
    Metal = 0,

    /// <summary>Plastic (Nhựa) — lightweight, variety of colors and styles</summary>
    Plastic = 1,

    /// <summary>Titanium (Titan) — premium lightweight, corrosion-resistant, hypoallergenic</summary>
    Titanium = 2
}
